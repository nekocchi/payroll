package model;

/**
 *
 * @author EdwardLeonardi
 */
public class User {
    private String ID;
    private String password;
    private String name;
    private int type;

    public User(String ID, String password, String name, int type) {
        this.ID = ID;
        this.password = password;
        this.name = name;
        this.type = type;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
